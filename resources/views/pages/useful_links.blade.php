                    <a href="http://acm.sgu.ru/"><br />
                        Saratov Online Judge</a> (over 300  problems, most problems are also rather challenging.)<br />
                    <a href="http://acm.tju.edu.cn/toj/"><br />
                        TJU Online Judge</a> (over 3000  problems. Most are from old regionals or similar. A cool feature on their judge  is the ability to have virtual contests, and since they have a lot of old  regionals, this is a very useful feature for team practice for upcoming  ACM/ICPC contests.)<br />
                    <a href="http://acm.pku.edu.cn/JudgeOnline/"><br />
                        Peking University Online  Judge</a> (more than 2700 problems.)<br />
                    <a href="http://acm.zju.edu.cn/"><br />
                        ZJU Online Judge</a> (over 2000  problems. Note that this judge supports only C and C++.)<br />
                    <a href="http://www.topcoder.com/tc"><br />
                        TopCoder</a> (TopCoder is not  actually an online judge. TopCoder is an organization that arranges programming  contests. They have their own arena with practice rooms and a lot of problems  from previous SRM's (single round matches). Moreover TopCoder arranges matches  almost weekly, often with prizes to the best scoring contestants. Many people  practice at TopCoder, and it is also highly encouraged.)<br />
                    <a href="http://ace.delos.com/usacogate/"><br />
                        USACO  Training Program Gateway</a>, this site is intended for high school students,  but is in fact used by many others. They have a nice training program with  tutorials, and reference solutions that you can see once you have solved a  problem. Their training program is highly recommended.<br />
                    <a href="http://acm.timus.ru/"><br />
                        Ural Online Judge</a> (over 650 problems, in general most problems are quite difficult.)<br />
                    <a href="http://www.spoj.pl"><br />
                        Sphere Online Judge</a>, over 500 problems,  this judge supports over 25 different programming languages. It excels in this  area. This is a widely used online judge.<br />
                    <a href="http://acm.mipt.ru/judge/login.pl?lang=en"><br />
                        Moscow  Online Judge</a> (few problems, but all of are of a high quality.)<br />
                    <a href="http://acm.fzu.edu.cn/"><br />
                        FZU Online Judge</a> (about 500  problems.)<br />
                    <a href="http://acm.hdu.edu.cn/"><br />
                        HDU Online Judge</a> (has virtual  contest features and quite a lot of problems.)<br />
                    <a href="http://acm.sharif.edu/arbiter/home.jsf">Arbiter</a> (online  judge at Sharif University  in Iran.)</p>
                <p dir="ltr"><strong>&nbsp;</strong></p>
                <h5 dir="ltr"><strong>Useful sites</strong></h5>
                <p dir="ltr"><a href="http://www.algorithmist.com/">Algorithmist</a> (here you can  find explanations of many problems and also classifications. There are also  nice tutorials about different subjects. Also they have a calendar of different  programming contests. You should really check this page out!)</p>
                <p dir="ltr"><a href="http://shygypsy.com/">Igor Naverniouk's site</a> (Igor has a  lot of experience in solving problems at the UVA judge. At his site you can see  how he has classified his solved problems, and in some cases you can also get  hints. His problem classification list is often very useful. His library of  algorithms (in C++) is very useful, and is definitely worth a look.)</p>
                <p dir="ltr"><a href="http://plg.uwaterloo.ca/%7Eacm00/">Waterloo Programming  Contests</a> (this is the local page for contests held at the University of Waterloo  in Canada.  Here you will find a lot of great problems (most of them are also on the UVA  judge), including solutions, input and output. Most solutions are of a very  high quality. If you are stuck on one of their problems, it may be a good idea  to take a look here.) </p>
                <p dir="ltr"><a href="http://fdemesmay.dyndns.org/cs/acm/">Mesmay's ACM-ICPC  Resources page</a> (here you can find hints for many of the problems of Volume  I at the UVA judge. You can also find extra input and output for these  problems. Furthermore there is a section concerning many of the standard  algorithms. This site is mainly useful if you are stuck on one of the problems  of Volume I at UVA) </p>
                <p dir="ltr"><a href="http://www.topcoder.com/tc?module=Static&amp;d1=tutorials&amp;d2=alg_index">TopCoder  Algorithm Tutorials</a> (here you can find about 40 good tutorials about useful  algorithms)</p>
                <p dir="ltr"><a href="http://wiki.pykello.net/doku.php">Hadi Moshayedi's site</a> (here you can  find some useful and ‌appropriate algorithms and solution‌)</p>
